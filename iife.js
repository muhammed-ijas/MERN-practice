
//IIFE = immediately invoked function expression  is a function in JavaScript that runs as soon as it is defined.

(()=>{
    console.log("its an iife function");
})()

// Self-Invoking: The function is executed immediately after it is defined.
// Scope Isolation: Variables defined inside the IIFE cannot be accessed outside, preventing them from interfering with the outer code.
// Anonymous or Named: An IIFE can be anonymous or named.